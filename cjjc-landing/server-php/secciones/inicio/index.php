		El Conservatorio de Música "Juan José Castro" es una institución estatal de nivel terciario, dependiente de la Dirección General de Cultura y Educación de la Provincia de Buenos Aires, que ofrece una formación integral en todos los niveles. 
		Brinda carreras docentes y tecnicaturas de 4 años, como así también la formación básica previa, a la que se puede ingresar sin conocimientos previos, y que según la edad del estudiante su duración varía entre 3 y 6 años.
