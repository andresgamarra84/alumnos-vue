<h3>Ingreso al Conservatorio</h3>
<div class='row p-2'>

	<p>El Conservatorio de Música "Juan José Castro" es una institución estatal de formación docente de nivel terciario, dependiente de la Dirección General de Cultura y Educación de la Provincia de Buenos Aires, que otorga títulos con validez nacional.</p>
	<p>Es una carrera docente de 4 años con una formación básica previa también se cursa en esta institución de 3 o 6 años, según la edad de ingreso. También se pueden cursar tecnicaturas con la misma formación básica. No son ni hay cursos breves, ni clases particulares, ni talleres libres.</p>
	<p>Es gratuito (sólo se solicita la cooperadora anual voluntaria)</p>

	
	<p>No hay examen de ingreso ni se requieren conocimientos previos.</p>
	
	<p>Se puede comenzar a estudiar desde los 9 años de edad.</p>
	
	<p>La cursada es anual, de mediados de Marzo a fines de Noviembre. Se cursa por materia, y la mayoría de ellas poseen opción de cursado en los turnos mañana, tarde y noche. El horario se oferta y selecciona en la inscripción. Se requiere asistencia, trabajos prácticos, exámenes cuatrimestrales y finales.</p>
	
	<p>Se pueden estudiar los siguientes instrumentos (el instrumento que se elija es para toda la carrera y, según la demanda de cada uno de ellos, se podría cursar más de uno)</p>
	<ul>
	<li>Arpa</li>
	<li>Bandoneón</li>
	<li>Canto lírico (a partir de los 14 años de edad</li>
	<li>Clarinete</li>
	<li>Contrabajo</li>
	<li>Corno</li>
	<li>Fagot </li>
	<li>Flauta dulce</li>
	<li>Flauta traversa</li>
	<li>Guitarra (Clásica - no se usa púa ni tablatura)</li>
	<li>Oboe</li>
	<li>Percusión</li>
	<li>Piano (no órgano ni teclado)</li>
	<li>Trompeta</li>
	<li>Trombón</li>
	<li>Saxofón</li>
	<li>Viola</li>
	<li>Violín</li>
	<li>Violonchelo</li>
	</ul>
	
	<p>Al comenzar los estudios en el Conservatorio, se ingresa al ciclo de iniciación a la música que llamamos FO.BA. (Formación Básica). Según la edad del ingresante, este primer ciclo tiene distinta duración, a saber:
	<ul>
	<li>Formación Básica para Niños y Adolescentes (entre 9 y 13 años de edad) - duración de 6 años</li>
	<li>Formación Básica para Jóvenes y Adultos (mayores de 14 años) - duración de 3 años</li>
	<li>Formación Básica para Jóvenes y Adultos (Canto Lírico) - duración de 3 años</li>
	</ul>
	</p>
		
	<p>Las materias que conforman el primer año varían a su vez según la edad y especialización que se elige:</p>
	
	<p>
	1er año FO.BA. Niños (9 a 13 años):
	<ul>
		<li>Taller de Iniciación Musical: materia donde se aprende a leer y escribir música. Se cursa 2 veces por semana, 1 hora y media por clase.</li>
		<li>Taller de Iniciación Instrumental: es la materia donde se hace la práctica con el instrumento. Se cursa 1 vez por semana.</li>
	</ul>
	</p>
	<p>
	1er año FO.BA. Jóvenes y Adultos - Instrumento (mayores de 14 años):
	<ul>
		<li>Lenguaje Musical: se aprende la base de la lectoescritura musical. Se cursa 2 veces por semana, 2 horas de duración por clase.</li>
		<li>Instrumento: es la materia donde se hace la&nbsp;práctica con el instrumento. Se cursa 1 vez por semana.</li>
	</ul>
	</p>
	<p>
	1er año FO.BA. Jóvenes y Adultos – Canto (mayores 14 años):
	<ul>
		<li>Lenguaje Musical idem FOBA Adultos de instrumento (cursan juntos).</li>
		<li>Canto: es la materia donde se hace la práctica con la voz. Se cursa 1 vez por semana.</li>
		<li>Dicción Italiana: se enseña la fonética del&nbsp;idioma. Se cursa 1 vez por semana – 2 horas.</li>
		<li>Trabajo Corporal: se ven ejercicios de relajación y corrección postural para mejorar la emisión del canto. 1 vez por semana – 2 horas.</li>
		<li>Foniatría: materia donde se enseñan características y cuidados de la garganta, oído, etc. Se cursa 1 vez por semana, 2 horas de duración.</li>
	</ul>
	</p>
	<p>Todas las materias son anuales y de cursada obligatoria.</p>
	<p>Luego de completar la FOBA, se puede comenzar el ciclo terciario de nuestra institución, conformado por las siguientes carreras:</p>
		
	<p>Profesorados y Tecnicaturas de:
	<ul>		
		<li>Instrumento</li>
		<li>Canto</li>
		<li>Dirección Coral</li>
	</ul>
	<p>
		
	<p>Profesorado de Educación Musical</p>
	<p>Puede cursarse más de una carrera. Todas tienen cuatro años de duración y para ingresar al profesorado es indispensable tener TÍTULO SECUNDARIO y la FOBA COMPLETA - ACREDITADA.</p>
</div>